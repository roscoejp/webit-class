		<a name="size" style="font-weight: bold;">Size</a>
		<div>
			The size of the free information community - and by extension the open
			source community - is enormous.
			<br /><br />
			Unfortunately, there isn't a singular 'right' way to quantify the number 
			of people who belong to the open source community.
			<br /><br />
			If you count only people who develope open source applications, then there
			certainly aren't that many (although, compared to the number of software
			developers, it certainly is a large percentage). 
			<br /><br />
			If, however, you count all those who <span class="u">use</span> open source
			software as belonging to the community, then every single person in the world
			could be counted amongst its members!
			<br /><br />
			<img src="images/open pie.jpg" alt="open source by country" width="450"
				style="float: left;"/>
			"But I'm not using any open source software!"
			<br /><br />
			If you're using the internet, then you've already accessed an open application.
			Since 60% of the hardware that runs the internet runs on open source software,
			there's also a good chance you have an open standard being used on your computer
			right now (maybe that song from the
			<a href="#url" onclick="window.open('player.html', 'mynewwindow', 'width=420,height=341')">background music player</a>
			, or the picture formats used on this website).
			<br /><br />
			Suffice to say, there's no solid number for the size of the open source
			community, other than that it's large.
			<br /><br />
			And it's growing... fast.
		</div>
		<br /><hr />
		<a name="thoughts" style="font-weight: bold;">Thoughts</a>
		<div>
			Because of the size of the open source community, it's no wonder that large,
			proprietary software manufacturers aren't all to happy with its existance.
			<br /><br />
			The reason the large manufacturer's are dismayed with the size of the community,
			is because, as the number of users of free applications increase, the number
			of 'developers' that could potentially contribute to that product, also increases.
			<br /><br />
			<img src="images/guevara stallman.jpg" alt="an open communist" 
				style="float: right; margin: 0 10px;"/>
			This, in turn, could lead to an open source product 'overtaking' it's closed source
			alternative in terms of features or stability (an active example would be Apache
			and Red Hat server distrobutions).
			<br /><br />
			This means that the proprietary software manufacturer would have to employ more developers,
			which in turn, costs more.
			<br /><br />
			This has led a few of the CEOs of the larger software manufacturers to dismiss the
			open source movement in various ways.
			<br /><br />
			Bill Gates, former head of Microsoft, called the open source community "...some new
			modern-day sort of communists."
			<br /><br />
			SAP AG's CEO Sha Agassi also criticised open source, saying that it would "fail to 
			deliver innovation" -- something that was actually seen more recently in a closed source
			application when Microsoft Windows 7 was released and found to have an almost identical
			kernel to its predecessor, Windows Vista, while more than doubling its retail price.
			<br /><br />
			Although manufacturers aren't too happy with the open source movement, there are many
			companies and orginasations who are more than happy to use open software. A study
			from January of 2010 showed that over 75% of large orginasations with over 1000
			employees from the US and UK used open source software across their entire enterprise.
		</div>
